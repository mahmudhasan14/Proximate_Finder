    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        try {
    // Load the MySQL JDBC Driver
    Class.forName("com.mysql.cj.jdbc.Driver");

    // Establish the connection
    Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/proximate_finder_dbms", "root", "");

    // SQL query to fetch all records from the hospital table
    String sql = "SELECT * FROM hospital";

    // Create a PreparedStatement
    PreparedStatement ptst = con.prepareStatement(sql);

    // Execute the query
    ResultSet rs = ptst.executeQuery();

    // Get the DefaultTableModel of jTable1
    DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();

    // Clear existing rows in the table
    dt.setRowCount(0);

    // Loop through the ResultSet and add rows to the table
    while (rs.next()) {
        Object[] row = {
            rs.getString("SI"),
            rs.getString("ZIP"),
            rs.getString("Name"),
            rs.getString("Opening_Time"),
            rs.getString("Closing_Time"),
            rs.getString("Contact_No"),
            rs.getString("Location")
        };
        dt.addRow(row);
    }

    // Close resources
    rs.close();
    ptst.close();
    con.close();

} catch (ClassNotFoundException e) {
    // Handle driver loading errors
    JOptionPane.showMessageDialog(this, "JDBC Driver not found: " + e.getMessage());
} catch (Exception e) {
    // Handle SQL-related errors
    JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
}

       
    }//GEN-LAST:event_jButton12ActionPerformed

